import numpy as np

# Polynomials
# Naming convention is (poly)(variables)(exponent for each variable)
# This should be possible to generalise


def poly200(x):
    return 1
def poly210(x):
    return x[0]
def poly201(x):
    return x[1]
def poly220(x):
    return x[0]**2
def poly202(x):
    return x[1]**2
def poly211(x):
    return x[0]*x[1]
